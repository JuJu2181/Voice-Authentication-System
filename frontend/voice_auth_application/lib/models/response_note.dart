import 'package:voice_auth_app/models/note.dart';

class ResponseNotes {
  final String status;
  final int code;
  final String? error;
  final String? success;
  final List<Note>? data;

  const ResponseNotes({
    required this.status,
    required this.code,
    required this.error,
    required this.success,
    required this.data
  });
  factory ResponseNotes.fromJson(Map<String, dynamic> json) {
    List<Note>? data = [];
    if (json['data'] != null){
      json['data'].forEach((d){
        data!.add(Note.fromJson(d));
      });
    }else{
      data = null;
    }
    return ResponseNotes(
      status: json['status'],
      code: json['code'],
      error: json['error'],
      success: json['success'],
      data: data
      // data: jsonDecode(json['data'])
    );
  }
}

class ResponseSingleNote {
  final String status;
  final int code;
  final String? error;
  final String? success;
  final Note? data;

  const ResponseSingleNote({
    required this.status,
    required this.code,
    required this.error,
    required this.success,
    required this.data
  });
  factory ResponseSingleNote.fromJson(Map<String, dynamic> json) {
    Note? data;
    if (json['data'] != null){
      data = Note.fromJson(json['data']);
    }else{
      data = null;
    }
    return ResponseSingleNote(
      status: json['status'],
      code: json['code'],
      error: json['error'],
      success: json['success'],
      data: data
      // data: jsonDecode(json['data'])
    );
  }
}
